/**
* @func   : DEBUG_PrintHex
* @brief  : 
* @param  : none
* @retval : none
*/

void DEBUG_PrintHex(uint8_t *data, uint16_t length){
	printf("Hex: ");
	for(uint16_t i = 0; i < length; i++){
		printf("0x%02X ", data[i]);
	}
	printf("\r\n");
}





